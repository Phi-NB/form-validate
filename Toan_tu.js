/**
    + (cộng), - (trừ) , *(nhân), / (chia), % (chia lấy số dư), ++ (Tăng giá trị lên 1), --(giảm giá trị đi 1) 
    * Lưu ý toán tử ++, --;
    Với trường hợp ++a (tiền tố)
    Việc 1: +1 cho a
    Việc 2: trả về a sau khi được cộng thêm 1

    Với trương hợp a++ (hậu tố)
    Việc 1: Copy biến a ra giọi là a copy
    Việc 2: cộng a với 1
    Việc 3: Trả về biến a copy
    
    vd: a = 6
    output = a++ + --a
    => 6 + 6 = 12
 */


